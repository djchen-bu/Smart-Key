/* An HTTP POST handler */
esp_err_t fob_post_handler(httpd_req_t *req)
{
    char buf[100];
    int ret, remaining = req->content_len;

    while (remaining > 0) {
        /* Read the data for the request */
        if ((ret = httpd_req_recv(req, buf,
                        MIN(remaining, sizeof(buf)))) <= 0) {
            if (ret == HTTPD_SOCK_ERR_TIMEOUT) {
                /* Retry receiving if timeout occurred */
                continue;
            }
            return ESP_FAIL;
        }

        /* Send back the same data */
        //httpd_resp_send_chunk(req, buf, ret);
        remaining -= ret;
    }
    if(atoi(buf)) {
        greenlight = true; // you now have access to a resource
        gpio_set_level(32,1);
    } else {
        greenlight = false; // you no longer have access to a resource
        gpio_set_level(32,0);
    }
    // End response
    //httpd_resp_send_chunk(req, NULL, 0);
    return ESP_OK;
}

